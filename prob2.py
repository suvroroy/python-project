
# Insert An Element At A Specific Position In An Array
# Given an array of numbers, insert a given element at the specified position in the array.

# Example One
# {
# "nums": [2, 4, 5, 6, -1],
# "element": 3,
# "position": 2
# }
# Output:
#
# [2, 3, 4, 5, 6]
# Example Two
# {
# "nums": [70, 60, 50, -1],
# "element": 40,
# "position": 4
# }
# Output:
#
# [70, 60, 50, 40]